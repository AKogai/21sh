void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int i;

	i = 0;
	if (!s || !(*s) || !f)
		return ;
	while (s && *s != '\0')
	{
		f(i, s);
		i++;
		s++;
	}
}
